namespace FME\CheckoutOrderAttributesFields\Controller\Adminhtml\Attribute;

class Index extends \FME\CheckoutOrderAttributesFields\Controller\Adminhtml\Attribute
{
    /**
     * @return \Magento\Backend\Model\View\Result\Page
     */
    public function execute()
    {
        $resultPage = $this->createActionPage();
        $resultPage->addContent(
            $resultPage->getLayout()->createBlock('FME\CheckoutOrderAttributesFields\Block\Adminhtml\Attribute')
        );
        return $resultPage;
    }
    
    /**
     * Determine if authorized to perform group actions.
     *
     * @return bool
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('FME_CheckoutOrderAttributesFields::attributes');
    }
}
